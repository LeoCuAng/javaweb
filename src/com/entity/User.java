package com.entity;  
  
public class User {  
    private int id;  
    private String name;  
    private String pwd;  
    private String sex;  
    private String dep;  
    private String info;
    private String pay;
    public int getId() {  
        return id;  
    }  
    public void setId(int id) {  
        this.id = id;  
    }  
    public String getName() {  
        return name;  
    }  
    public void setName(String name) {  
        this.name = name;  
    }  
    public String getPwd() {  
        return pwd;  
    }  
    public void setPwd(String pwd) {  
        this.pwd = pwd;  
    }  
    public String getSex() {  
        return sex;  
    }  
    public void setSex(String sex) {  
        this.sex = sex;  
    }  
    public String getDep() {  
        return dep;  
    }  
    public void setdep(String dep) {  
        this.dep = dep;  
    }  
    public String getInfo() {  
        return info;  
    }  
    public void setInfo(String info) {  
        this.info = info;  
    } 
    public String getPay() {  
        return pay;  
    }  
    public void setPay(String pay) {  
        this.pay = pay;  
    }  
      
}